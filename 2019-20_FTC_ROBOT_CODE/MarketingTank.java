import com.qualcomm.robotcore.eventloop.opmode.Disabled;
import com.qualcomm.robotcore.eventloop.opmode.LinearOpMode;
import com.qualcomm.robotcore.eventloop.opmode.TeleOp;
import com.qualcomm.robotcore.util.Range;

import org.firstinspires.ftc.teamcode.MarketingHardware;

/**
 * This OpMode uses the common Pushbot hardware class to define the devices on the robot.
 * All device access is managed through the HardwarePushbot class.
 * The code is structured as a LinearOpMode
 *
 * This particular OpMode executes a POV Game style Teleop for a PushBot
 * In this mode the left stick moves the robot FWD and back, the Right stick turns left and right.
 * It raises and lowers the claw using the Gampad Y and A buttons respectively.
 * It also opens and closes the claws slowly using the left and right Bumper buttons.
 *
 * Use Android Studios to Copy this Class, and Paste it into your team's code folder with a new name.
 * Remove or comment out the @Disabled line to add this opmode to the Driver Station OpMode list
 */

@TeleOp(name="Marketing-Bot Tank Drive", group="Marketing")
//@Disabled
public class MarketingTank extends LinearOpMode {

    /* Declare OpMode members. */
    MarketingHardware robot           = new MarketingHardware();   // Use a Pushbot's hardware

    public double joystickShiftR = 0.033333;
    public double joystickShiftL = 0.0033333;
    public double joystickScaleR = 5;
    public double joystickScaleL = 5.5;

    @Override
    public void runOpMode() {

        /* Initialize the hardware variables.
         * The init() method of the hardware class does all the work here
         */
        robot.init(hardwareMap);

        // Send telemetry message to signify robot waiting;
        telemetry.addData("Say", "Hello Driver");    //
        telemetry.update();

        // Wait for the game to start (driver presses PLAY)
        waitForStart();

        // run until the end of the match (driver presses STOP)
        while (opModeIsActive()) {

            double leftPower = -gamepad1.right_stick_y / joystickScaleL + joystickShiftL;
            double rightPower = -gamepad1.left_stick_y / joystickScaleR + joystickShiftR;

            //Send Joystick Values to Drive Motors
            robot.left.setPower(leftPower);
            robot.right.setPower(rightPower);

            // Send telemetry message to signify robot running;
            telemetry.addData("Left Power", robot.left.getPower());
            telemetry.addData("Right Power", robot.right.getPower());
            telemetry.update();

        }
    }
}
